public class gfgArrayTraversal{


public static void arrayTraversal(int[] arr) {
       
        for(int i=0;i<arr.length;i++){
            System.out.print(arr[i]+" ");
        }
    }




    public static void main(String[] args) {
        int[] arr={1,2,43,54,6,7,87};
        arrayTraversal(arr);
    }
}